# Diberikan list
buah = ['apel', 'mangga', 'jeruk', 'anggur', 'pisang']
print(f"List awal buah = {buah}")
print()

# a. Tambahkan 'semangka' di akhir list
print("a. Tambahkan 'semangka' di akhir list")
buah.append('semangka')
print(f"   Hasil: {buah}")
print()

# b. Sisipkan 'durian' di antara 'jeruk' dan 'anggur'
print("b. Sisipkan 'durian' di antara 'jeruk' dan 'anggur'")
# Index 'jeruk' = 2, maka sisipkan di index 3 (setelah jeruk)
buah.insert(3, 'durian')
print(f"   Hasil: {buah}")
print()

# c. Hapus 'mangga' dari list
print("c. Hapus 'mangga' dari list")
buah.remove('mangga')
print(f"   Hasil: {buah}")
print()

# d. Ubah 'pisang' menjadi 'nanas'
print("d. Ubah 'pisang' menjadi 'nanas'")
# Cari posisi index 'pisang' kemudian ubah
index_pisang = buah.index('pisang')
buah[index_pisang] = 'nanas'
print(f"   Index pisang = {index_pisang}")
print(f"   Hasil: {buah}")
print()

# e. Tampilkan 3 buah pertama
print("e. Tampilkan 3 buah pertama")
tiga_buah_pertama = buah[:3]
print(f"   Hasil: {tiga_buah_pertama}")
print()

