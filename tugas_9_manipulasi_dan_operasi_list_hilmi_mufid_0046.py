# Diberikan list
angka = [45, 12, 78, 23, 56, 89, 34]
print(f"List awal angka = {angka}")
print()

# a. Urutkan list secara ascending
print("a. Urutkan list secara ascending")
angka_ascending = angka.copy()  # membuat salinan agar list asli tidak berubah
angka_ascending.sort()
print(f"   Hasil sort ascending: {angka_ascending}")
print()

# b. Urutkan list secara descending
print("b. Urutkan list secara descending")
angka_descending = angka.copy()  # membuat salinan
angka_descending.sort()
angka_descending.reverse()  # membalik list setelah di-sort
print(f"   Hasil sort descending: {angka_descending}")
print()